// Program to print sum,multiplication,subtraction,division & remainder of two numbers.

public class Calculations {
  public static void main(String[] args){

// Assigning values:
   int num1 = 10;
   int num2 = 5;

// performing calculation and storing in a new variable:
   int sum = num1 + num2;
   int mul = num1 * num2;
   int sub = num1 - num2;
   int div = num1 / num2;
   int rem = num1 % num2;

//printing the value of the new variables for answers:
   System.out.println(sum);
   System.out.println(mul);
   System.out.println(sub);
   System.out.println(div);
   System.out.println(rem);
 }
}